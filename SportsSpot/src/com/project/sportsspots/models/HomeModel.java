package com.project.sportsspots.models;

public class HomeModel {
	
	private String newsId;
	private String newsTitle;
	private String newsDescription;
	private String newsImage;
	private String newsAddedDate;
	private String newsPriority;
	private String newsTitleAr;
	private String newsDescriptionAr;

	
	public String getNewsId() {
		return newsId;
	}
	public void setNewsId(String newsId) {
		this.newsId = newsId;
	}
	public String getNewsTitle() {
		return newsTitle;
	}
	public void setNewsTitle(String newsTitle) {
		this.newsTitle = newsTitle;
	}
	public String getNewsDescription() {
		return newsDescription;
	}
	public void setNewsDescription(String newsDescription) {
		this.newsDescription = newsDescription;
	}
	public String getNewsImage() {
		return newsImage;
	}
	public void setNewsImage(String newsImage) {
		this.newsImage = newsImage;
	}
	public String getNewsAddedDate() {
		return newsAddedDate;
	}
	public void setNewsAddedDate(String newsAddedDate) {
		this.newsAddedDate = newsAddedDate;
	}
	public String getNewsPriority() {
		return newsPriority;
	}
	public void setNewsPriority(String newsPriority) {
		this.newsPriority = newsPriority;
	}
	public String getNewsTitleAr() {
		return newsTitleAr;
	}
	public void setNewsTitleAr(String newsTitleAr) {
		this.newsTitleAr = newsTitleAr;
	}
	public String getNewsDescriptionAr() {
		return newsDescriptionAr;
	}
	public void setNewsDescriptionAr(String newsDescriptionAr) {
		this.newsDescriptionAr = newsDescriptionAr;
	}

}
