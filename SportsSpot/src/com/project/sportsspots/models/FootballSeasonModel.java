package com.project.sportsspots.models;

public class FootballSeasonModel {
	
	private String footballId;
	private String footballCaption;
	private String footballLeague;
	private String footballYear;
	private String footballLastUpdated;
	public String getFootballId() {
		return footballId;
	}
	public void setFootballId(String footballId) {
		this.footballId = footballId;
	}
	public String getFootballCaption() {
		return footballCaption;
	}
	public void setFootballCaption(String footballCaption) {
		this.footballCaption = footballCaption;
	}
	public String getFootballLeague() {
		return footballLeague;
	}
	public void setFootballLeague(String footballLeague) {
		this.footballLeague = footballLeague;
	}
	public String getFootballYear() {
		return footballYear;
	}
	public void setFootballYear(String footballYear) {
		this.footballYear = footballYear;
	}
	public String getFootballLastUpdated() {
		return footballLastUpdated;
	}
	public void setFootballLastUpdated(String footballLastUpdated) {
		this.footballLastUpdated = footballLastUpdated;
	}

}
